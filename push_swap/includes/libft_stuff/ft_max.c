int	ft_max(int a, int b)
{
	if (a >= 0 && b >= 0)
	{
		if (a > b)
			return (a);
		return (b);
	}
	else if (a <= 0 && b <= 0)
	{
		if (a > b)
			return (b);
		return (a);
	}
	else
		return (0);
}
